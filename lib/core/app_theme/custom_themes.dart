import 'package:flutter/material.dart';

class CustomThemes {

  static TextStyle primaryColorTextTheme(context) =>
      Theme.of(context).textTheme.headlineLarge!;
  static TextStyle greyColor797TextTheme(context) =>
      Theme.of(context).textTheme.headlineMedium!;
  static TextStyle whiteColorTextTheme(context) =>
      Theme.of(context).textTheme.headlineSmall!;
  static TextStyle greyColorC6TextTheme(context) =>
      Theme.of(context).textTheme.bodyLarge!;
  static TextStyle greyColor49TextTheme(context) =>
      Theme.of(context).textTheme.bodyMedium!;
}